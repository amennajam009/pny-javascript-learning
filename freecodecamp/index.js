// today we're gonna know about the switch statement

function casenoone(val){
    let result = ""
    switch(val){
       case 1:
        result="yes it works";
        break;
        case 2:
            result="no it's not working";
            break;
        case 3:
            result="yup it absolutely working";
            break;
            
            
    }
    return result;
}
casenoone(2);